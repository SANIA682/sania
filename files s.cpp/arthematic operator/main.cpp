#include <iostream>
using namespace std;
int main()
{  int a = 10, b = 3;
   cout<< " a + b = "<< a + b <<endl;
   cout<< " a - b = "<< a - b <<endl;
   cout<< " a * b = "<< a * b <<endl;
   cout<< " a / b = "<< a / b <<endl;
   cout<< " a % b = "<< a % b <<endl;
   cout<< " ++a = "<< ++a <<endl;
   cout<<a<<endl;
   cout<< " b-- = "<< b-- <<endl;
   cout<<" after b--, b ="<<b<<endl;

    return 0;
}
